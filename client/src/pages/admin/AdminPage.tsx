const AdminPage: React.FC = () => {
  const [searchParams, setSearchParams] = useSearchParams();
  const currentTab = searchParams.get('tab') || 'cinemas';

  // Validate tab and fallback to default
  const validTabs: TabId[] = ['cinemas', 'rapports', 'users', 'settings', 'system'];
  const activeTab: TabId = validTabs.includes(currentTab as TabId) 
    ? (currentTab as TabId) 
    : 'cinemas';

  const handleTabClick = (tabId: TabId) => {
    setSearchParams({ tab: tabId });
  };

  return (
    <div className="max-w-7xl mx-auto">
      {/* Page title */}
      <div className="mb-6">
        <h1 className="text-3xl md:text-4xl font-bold">Administration</h1>
      </div>

      {/* Tabs navigation */}
      <div className="border-b border-gray-200 mb-6 sticky top-0 bg-white z-10">
        <nav className="flex space-x-8" role="tablist">
          {tabs.map((tab) => (
            <button
              key={tab.id}
              role="tab"
              aria-selected={activeTab === tab.id}
              onClick={() => handleTabClick(tab.id)}
              className={`
                flex items-center gap-2 pb-4 border-b-2 font-medium transition-colors
                ${
                  activeTab === tab.id
                    ? 'border-primary text-primary'
                    : 'border-transparent text-gray-500 hover:text-gray-700 hover:border-gray-300'
                }
              `}
            >
              {tab.icon}
              <span>{tab.label}</span>
            </button>
          ))}
        </nav>
      </div>

      {/* Tab content */}
      <div role="tabpanel">
        {activeTab === 'cinemas' && <CinemasPage />}
        {activeTab === 'rapports' && <ReportsPage />}
        {activeTab === 'users' && <UsersPage />}
        {activeTab === 'settings' && <SettingsPage />}
        {activeTab === 'system' && <SystemPage />}
      </div>
    </div>
  );
};

export default AdminPage;
